    <script>
        // Search Functionality
        const searchInput = document.querySelector('.search-input');
        const vehicleCards = document.querySelectorAll('.car-wrap');

        searchInput.addEventListener('input', function() {
            const searchQuery = this.value.toLowerCase();

            vehicleCards.forEach(function(card) {
                const vehicleName = card.querySelector('h2').textContent.toLowerCase();
                if (vehicleName.includes(searchQuery)) {
                    card.style.display = 'block';
                } else {
                    card.style.display = 'none';
                }
            });
        });

        // Filter Functionality
        const filterBtns = document.querySelectorAll('.filter-btn');

        filterBtns.forEach(function(btn) {
            btn.addEventListener('click', function() {
                const minPrice = parseInt(this.dataset.min) || 0;
                const maxPrice = parseInt(this.dataset.max) || Infinity;

                filterBtns.forEach(function(btn) {
                    btn.classList.remove('active');
                });
                this.classList.add('active');

                vehicleCards.forEach(function(card) {
                    const priceText = card.querySelector('p:last-child').textContent;
                    const price = parseInt(priceText.replace(/[^0-9]/g, ''));
                    if (price >= minPrice && price <= maxPrice) {
                        card.style.display = 'block';
                    } else {
                        card.style.display = 'none';
                    }
                });
            });
        });
    </script>
